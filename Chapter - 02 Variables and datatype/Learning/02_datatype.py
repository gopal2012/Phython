# Numeric data types
num1 = 5  # int
num2 = 2.0  # float
num3 = 1 + 2j  # complex

# String data type
language = "Python"  # str

# List (sequence type)
languages = ["Swift", "Java", "Python"]  # list

# Tuple (sequence type)
product = ('Xbox', 499.99)  # tuple

# Dictionary (mapping type)
person = {"name": "Alice", "age": 30}  # dict

# Boolean data type
is_valid = True  # bool

# Set data type
unique_numbers = {1, 2, 3}  # set
