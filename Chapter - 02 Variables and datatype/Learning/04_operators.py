# 1. Arithmetic operator

# Addition
result_add = 5 + 3
print("Addition:", result_add)  # Output: 8

# Subtraction
result_sub = 10 - 4
print("Subtraction:", result_sub)  # Output: 6

# Multiplication
result_mul = 3 * 7
print("Multiplication:", result_mul)  # Output: 21

# Division
result_div = 15 / 3
print("Division:", result_div)  # Output: 5.0 (float)

# 2.Assignment operator
# Simple assignment
x = 10

# Addition assignment
x += 5  # Equivalent to x = x + 5
print("Updated x:", x)  # Output: 15

# Subtraction assignment
y = 20
y -= 3  # Equivalent to y = y - 3
print("Updated y:", y)  # Output: 17

# 3.Comparison operator
# Equal to
a = 5
b = 5
print("a == b:", a == b)  # Output: True

# Greater than
x = 8
y = 6
print("x > y:", x > y)  # Output: True

# Not equal to
c = 10
d = 7
print("c != d:", c != d)  # Output: True


# 4.Logical Operator
# Logical AND
is_weekend = True
is_holiday = False
result_and = is_weekend and is_holiday
print("Weekend and holiday:", result_and)  # Output: False

# Logical OR
is_sunny = False
result_or = is_weekend or is_sunny
print("Weekend or sunny:", result_or)  # Output: True

# Logical NOT
is_raining = False
result_not = not is_raining
print("Not raining:", result_not)  # Output: True
